/*==================== LANGUAGE TOGGLE ====================*/
window.langToggle = document.getElementById('lang-toggle');
const htmlEl = document.documentElement;

// Function to set Language
export function setLanguage(lang) {
    if (lang === 'ar') {
        htmlEl.setAttribute('dir', 'rtl');
        htmlEl.setAttribute('lang', 'ar');
        document.body.classList.add('rtl');
    } else {
        htmlEl.setAttribute('dir', 'ltr');
        htmlEl.setAttribute('lang', 'en');
        document.body.classList.remove('rtl');
    }

    // Update text content
    const elements = document.querySelectorAll('[data-i18n]');
    elements.forEach(el => {
        const key = el.getAttribute('data-i18n');
        if (translations[lang][key]) {
            if (el.tagName === 'INPUT' || el.tagName === 'TEXTAREA') {
                el.placeholder = translations[lang][key];
            } else {
                el.innerHTML = translations[lang][key];
            }
        }
    });

    // Update TypeWriter if exists
    if (window.typeWriterInstance && translations[lang].heroTyping) {
        window.typeWriterInstance.updateWords(JSON.parse(translations[lang].heroTyping));
    }

    // Save preference
    localStorage.setItem('selected-lang', lang);
    
    // Update Toggle UI check if exists
    if(langToggle) {
        const langText = langToggle.querySelectorAll('.lang-text');
        langText.forEach(span => {
            if(span.textContent.toLowerCase() === lang) {
                span.style.fontWeight = 'bold';
                span.style.color = 'var(--first-color)';
            } else {
                span.style.fontWeight = 'normal';
                span.style.color = 'var(--title-color)';
            }
        });
    }
}

// Initial Load
const savedLang = localStorage.getItem('selected-lang') || 'en';
setLanguage(savedLang);

// Toggle Event
if(langToggle) {
    langToggle.addEventListener('click', () => {
        const currentLang = htmlEl.getAttribute('lang');
        const newLang = currentLang === 'en' ? 'ar' : 'en';
        setLanguage(newLang);
    });
}
